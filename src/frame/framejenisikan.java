    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if(t1.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "id_ikan Belum DIisi");
                t1.requestFocus();
            } else if(t2.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "id_penjual Admin Belum Diisi");
                t2.requestFocus();
            } else if(t3.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "nm_ikan Belum Diisi");
                t3.requestFocus();
            } else if(t4.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "kategori Belum Diisi");
                t4.requestFocus();
            } else if(t5.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "harga Belum Diisi");
                t5.requestFocus();
            } else if(t6.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "stok Belum Diisi");
                t6.requestFocus();
            } else {
                mycode.simpanIkan(t1.getText(), t2.getText(), t3.getText(), t4.getText(), t5.getText(), t6.getText() );
                mycode.tampilDataIkan(tabelIkan, "SELECT*FROM jenis_ikan");
                if (mycode.duplikasi==true){
                    t3.setText(mycode.CEK_NAMA_IKAN);
                    t4.setText(mycode.CEK_KATEGORI);
                    t5.setText(mycode.CEK_HARGA);
                    t6.setText(mycode.CEK_STOK);
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

