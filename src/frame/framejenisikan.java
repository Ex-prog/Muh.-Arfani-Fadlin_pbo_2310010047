        String cari = "SELECT*FROM jenis_ikan WHERE id_ikan LIKE '%"+c+"%'"+
        "or id_penjual LIKE '%"+c+"%' or nama_ikan LIKE '%"+c+"%' or kategori LIKE '%"+c+"%' or harga LIKE '%"+c+"%' or stok LIKE '%"+c+"%'";
